	public class TeamN
	{
		private int	teamNumber;
		private String teamName;

		TeamN(int aTeamNumber,String aTeamName)
		{
			teamNumber = aTeamNumber;
			teamName = aTeamName;
		}
		
		public int getTeamNumber()
		{
			return teamNumber;
		}
		public String getTeamName()
		{	
			return teamName;
		}
	}